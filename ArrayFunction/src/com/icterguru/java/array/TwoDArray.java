package com.icterguru.java.array;

public class TwoDArray {
	public static void main(String[]args) {
	
	// Create 2-dimensional array.
	  int[][] twoD = new int[4][4];

	
	  // Assign three elements in it.
	  twoD[0][0] = 1;
	  twoD[1][1] = 2;
	  twoD[3][2] = 3;
	  System.out.print(twoD[0][0] + " ");
	}
	
	}


